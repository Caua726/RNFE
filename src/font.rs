pub fn draw_str(fb: &mut [u8], w: u32, h: u32, s: &str, scale: i32, rgba: [u8; 4]) {
    let (w_i, h_i) = (w as i32, h as i32);
    let char_w = 5 * scale;
    let spacing = 1 * scale;
    let text_w = (s.len() as i32) * (char_w + spacing) - spacing;
    let x0 = (w_i - text_w) / 2;
    let y0 = (h_i - 7 * scale) / 2;
    draw_str_at(fb, w, h, s, x0, y0, scale, rgba);
}

pub fn draw_str_at(fb: &mut [u8], w: u32, h: u32, s: &str, x0: i32, y0: i32, scale: i32, rgba: [u8; 4]) {
    let (w_i, h_i) = (w as i32, h as i32);
    let char_w = 5 * scale;
    let spacing = 1 * scale;

    let mut x = x0;
    for ch in s.chars() {
        let g = fonte(ch.to_ascii_uppercase());
        for (row, bits) in g.iter().enumerate() {
            for col in 0..5 {
                if (bits >> (4 - col)) & 1 == 1 {
                    for dy in 0..scale {
                        for dx in 0..scale {
                            let px = x + col as i32 * scale + dx;
                            let py = y0 + row as i32 * scale + dy;
                            if px >= 0 && py >= 0 && px < w_i && py < h_i {
                                let idx = ((py as u32 * w + px as u32) * 4) as usize;
                                fb[idx..idx + 4].copy_from_slice(&rgba);
                            }
                        }
                    }
                }
            }
        }
        x += char_w + spacing;
    }
}

pub fn text_width(s: &str, scale: i32) -> i32 {
    let char_w = 5 * scale;
    let spacing = 1 * scale;
    (s.len() as i32) * (char_w + spacing) - spacing
}

pub fn draw_rect(fb: &mut [u8], w: u32, h: u32, x: i32, y: i32, rw: i32, rh: i32, rgba: [u8; 4]) {
    let (w_i, h_i) = (w as i32, h as i32);
    for py in y..y + rh {
        for px in x..x + rw {
            if px >= 0 && py >= 0 && px < w_i && py < h_i {
                let idx = ((py as u32 * w + px as u32) * 4) as usize;
                fb[idx..idx + 4].copy_from_slice(&rgba);
            }
        }
    }
}
